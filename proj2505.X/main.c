void changeLed(int *IORegisters)
{
    int i;
    int temp = IORegisters[1];
    for (i=0; i<8; i++)
    {
        LED_SetValue(i, temp&1);
        temp = temp>>1;
    }
}

int execute_command(struct command newComm, int *regs, int *IORegisters, int *pc)
{
	if (newComm.opcode == 0 && (newComm.rd != 0))		// now we follow the command
		add(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt]);
	else if (newComm.opcode == 1 && (newComm.rd != 0))
		sub(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt]);
	else if (newComm.opcode == 2 && (newComm.rd != 0))
		andCom (&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt]);
	else if (newComm.opcode == 3 && (newComm.rd != 0))
		orCom (&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt]);
	else if (newComm.opcode == 4 && (newComm.rd != 0))
		sll(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt]);
	else if (newComm.opcode == 5 && (newComm.rd != 0))
		sra(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt]);
    else if (newComm.opcode == 6)
    {
        if (((newComm.rt == 0) && (newComm.rd!=0))||((newComm.rt!= 0)&&(regs[newComm.rs] == 1)))
    		newOp(&regs[newComm.rd], &IORegisters[regs[newComm.rs]], &regs[newComm.rt]);
        if ((newComm.rt) != 0 && (newComm.rs == 1))
            changeLed(IORegisters);
    }
	else if (newComm.opcode == 7)
		beq(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt], pc);
	else if (newComm.opcode == 8)
		bgt(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt], pc);
	else if (newComm.opcode == 9)
		ble(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt], pc);
	else if (newComm.opcode == 10)
		bne(&regs[newComm.rd], &regs[newComm.rs], &regs[newComm.rt], pc);
	else if (newComm.opcode == 11)
		jal(&regs[newComm.rd], pc, &regs[15]);
	else if (newComm.opcode == 12 && (newComm.rd != 0))
		lw(&memArr[(regs[newComm.rs] + regs[newComm.rt])&0x7FF], &regs[newComm.rd]);
	else if (newComm.opcode == 13)
        sw(&memArr[(regs[newComm.rs] + regs[newComm.rt])&0x7FF], &regs[newComm.rd]);
	else if (newComm.opcode == 14 && (newComm.rd != 0))
		lhi(&regs[newComm.rd], &regs[newComm.rs]);
	else if (newComm.opcode == 15)
		return 0;
	return 1;
}

void testImm(struct command newComm, int *pc)
{
    if (newComm.rt == 0x0001 || newComm.rs == 0x0001 || newComm.rd == 0x0001)	// check if we use imm 
    {
        (*pc)++;
        regs[1] = memArr[*pc];
    }
    else
        regs[1] = 0;
    if ((regs[1] & 0x8000) == (0x8000))					// if the imm is negative number
    	regs[1] = regs[1] | 0xFFFF0000;

}

void updateComm(struct command *newComm, int *pc)
{
    newComm->binCommand = memArr[*pc];
    newComm->rt = (newComm->binCommand) & (0x000F);						// to get the registers' numbers 
    newComm->rs = ((newComm->binCommand) & (0x00F0)) >> 4;
    newComm->rd = ((newComm->binCommand) & (0x0F00)) >> 8;
    newComm->opcode = ((newComm->binCommand) & (0xF000)) >> 12;		// to get the opcode of the command
}

void newCommFunc(struct command *newComm, int *pc)
{
    updateComm(newComm, pc);
    testImm(*newComm, pc);
}

void initComm(struct command *newComm, int *pc)
{
    *pc = 0;    // first command in memory
    newCommFunc(newComm, pc);
}

void displayOnScreen(int *lastSWState, char *strToLCD, int *count, int *firstMemSW, struct command newComm)
{
        if (SWT_GetValue(0) && SWT_GetValue(1))
        {
            if (*lastSWState != 3)
            {
                *lastSWState = 3;
                LCD_DisplayClear();
            }
            sprintf(strToLCD, "%d",*count);
            LCD_WriteStringAtPos(strToLCD, 0, 0);
        }
        if (!SWT_GetValue(0) && !SWT_GetValue(1))
        {
            if (*lastSWState != 0)
            {
                *lastSWState = 0;
                LCD_DisplayClear();
            }
            sprintf(strToLCD, "%04X", (regs[1] & 0xFFFF));
            LCD_WriteStringAtPos(strToLCD, 0, 0);
            sprintf(strToLCD, "%04X", (newComm.binCommand & 0xFFFF));
            LCD_WriteStringAtPos(strToLCD, 0, 4);
            
        }
        if (SWT_GetValue(0) && !SWT_GetValue(1))
        {
            if (*lastSWState != 1)
            {
                *lastSWState = 1;
                LCD_DisplayClear();
                registerToShow = 2;
            }
            sprintf(strToLCD,"R%02X = %08X",registerToShow, regs[registerToShow]);
            
            LCD_WriteStringAtPos(strToLCD, 0, 0);
        }
        if (!SWT_GetValue(0) && SWT_GetValue(1))
        {
            if (*lastSWState != 2)
            {
                *lastSWState = 2;
                LCD_DisplayClear();
                *firstMemSW = -1;
            }            
            if (SWT_GetValue(6))
            {
                if ((*firstMemSW)!=2)
                {
                    memToShow = 2047;
                    *firstMemSW=2;
                }
            }
            else
            {
                if (SWT_GetValue(5))
                {
                    if ((*firstMemSW)!=1)
                    {
                        memToShow = 1024;
                        *firstMemSW = 1;
                    }
                }
                else
                {
                    if ((*firstMemSW)!=0)
                    {
                        memToShow = 0;
                        *firstMemSW = 0;
                    }
                }
            }
            sprintf(strToLCD, "M%04X = %04X", memToShow, memArr[memToShow]);
            LCD_WriteStringAtPos(strToLCD,0,0);
            sprintf(strToLCD, "RSP = %08X", regs[13]);
            LCD_WriteStringAtPos(strToLCD,1,0);
        }
        
}

void scanArr(int *pc, int *regs, int *IORegisters)
{
    struct command newComm;
    int flag = 1;
    int count = 0;
    int firstMemSW = -1;
    int lastSWState = -1;
    char strToLCD[40];
    initComm(&newComm, pc); 
    while (flag == 1)
	{
        displayOnScreen(&lastSWState, strToLCD, &count, &firstMemSW, newComm);
        if (executeFlag == 1)
        {    
            flag = execute_command(newComm, regs, IORegisters, pc);
            count++;
            executeFlag = 0;
            (*pc)++;
            if (newComm.opcode != 15)
                newCommFunc(&newComm, pc);
        }
	}
    while (flag == 0)
        displayOnScreen(&lastSWState, strToLCD, &count, &firstMemSW, newComm);
}

void Timer5Setup()
{
  PR5 = 12500;   //set period register, generates one interrupt every 10 ms                     
  TMR5 = 0;                           //    initialize count to 0
  T5CONbits.TCKPS = 3;                //    divide by 8
  T5CONbits.TGATE = 0;                //    not gated input (the default)
  T5CONbits.TCS = 0;                  //    PCBLK input (the default)
  IPC5bits.T5IP = 2;                  //    INT step 4: priority
  IPC5bits.T5IS = 0;                  //    subpriority
  IFS0bits.T5IF = 0;                  //    clear interrupt flag
  IEC0bits.T5IE = 1;                  //    enable interrupt
  T5CONbits.ON = 1;                   //    turn on Timer5
  macro_enable_interrupts();          //    enable interrupts at CPU
}

void Timer4Setup()
{
    PR4 = 2441;                        //             set period register, generates one interrupt every 62.5ms   
    TMR4 = 0;                           //             initialize count to 0
    T4CONbits.TCKPS = 7;                //             divide by 256
    T4CONbits.TGATE = 0;                //             not gated input (the default)
    T4CONbits.TCS = 0;                  //             PCBLK input (the default)
    T4CONbits.ON = 1;                   //             turn on Timer1
    IPC4bits.T4IP = 2;                  //             priority
    IPC4bits.T4IS = 0;                  //             subpriority
    IFS0bits.T4IF = 0;                  //             clear interrupt flag
    IEC0bits.T4IE = 1;                  //             enable interrupt
    macro_enable_interrupts();
}

void __ISR(_TIMER_5_VECTOR, ipl2) Timer5ISR(void) 
{  
    if (BTN_GetValue(1))    //BTNL is pressed
    {
        if (BTNLflag == 0)
            BTNLflag = 1;
    }
    if (!BTN_GetValue(1))
    {
        if (BTNLflag == 1)
        {
            BTNLflag = 0;
            if (pauseFlag == 1)
                pauseFlag = 0;
            else
                pauseFlag = 1;
        }
    } 
    if (BTN_GetValue(2))    //BTNC is pressed
    {
        if (BTNCflag == 0)
            BTNCflag = 1;
    }
    if (!BTN_GetValue(2))
    {
        if (BTNCflag == 1)
        {
            BTNCflag = 0;
            IORegisters[2]++;
        }
    }
    
    if (BTN_GetValue(0))    //BTNU is pressed
    {
        if (BTNUflag == 0)
            BTNUflag = 1;
    }
    if (!BTN_GetValue(0))
    {
        if (BTNUflag == 1)
        {
            BTNUflag = 0;
            if (registerToShow == 15)
                registerToShow = 0;
            else
                registerToShow++;
            if (memToShow == 0x07FF)
                memToShow = 0;
            else
                memToShow++;
        }
    }
    
    if (BTN_GetValue(4))    //BTND is pressed
    {
        if (BTNDflag == 0)
            BTNDflag = 1;
    }
    if (!BTN_GetValue(4))
    {
        if (BTNDflag == 1)
        {
            BTNDflag = 0;
            if (registerToShow == 0)
                registerToShow = 15;
            else
                registerToShow--;
            if (memToShow == 0)
                memToShow = 0x07FF;
            else
                memToShow--;
        }
    }
    
    if (BTN_GetValue(3))    //BTNR is pressed
    {
        if (BTNRflag == 0)
            BTNRflag = 1;
    }
    if (!BTN_GetValue(3))
    {
        if (BTNRflag == 1)
        {
            BTNRflag = 0;
            if (pauseFlag == 1 && executeFlag == 0)
                executeFlag = 1;
        }
    }
    
    IFS0bits.T5IF = 0;     // clear interrupt flag
}

void __ISR(_TIMER_4_VECTOR, ipl2auto) Timer4ISR(void) 
{
    if (IORegisters[0] == 0xFFFFFFFF)
        IORegisters[0] = 0;
    else
        IORegisters[0]++;
    if (pauseFlag == 0 && executeFlag == 0)
        executeFlag = 1;
    IFS0bits.T4IF = 0;     // clear interrupt flag
}

